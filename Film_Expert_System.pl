% Predicate to read user input and provide recommendations recursively

recommendation_system :-
    writeln('Welcome to the Film Recommendation System!'),
    writeln('Could You please enter your name ??'),
    read(Name),
    write('Helloooooo, '),write(Name),write(' !'),nl,
    writeln('Please choose one of the following : '),
    writeln('1-Menu to choose your own criteria. '),
    writeln('2-General questions recommendations. '),
    read(Choice),
    Choice == 1 ->  findall(Film, film(Film), AllFilms),
    				query_preferences(AllFilms)  ; personalInfo_based_Recommendation.

personalInfo_based_Recommendation :-
    
    writeln('Enter Your Age :'),
    read(Age),
    writeln('Enter Your gender :'),
	read(Gender),
	writeln('What is your mood right now (happy - sad - bored - normal) ?'),
    read(Mood),
    writeln('What is the minimum Movie Rating you normally watch ?'),
    read(Rating),
    
    findall(Film, film(Film), AllFilms),
    
    % Apply filters sequentially
    filter_by_age(Age, AllFilms, AgeFilteredFilms),
    filter_by_gender(Gender, AgeFilteredFilms, GenderFilteredFilms),
    filter_by_mood(Mood, GenderFilteredFilms, MoodFilteredFilms),
    filter_by_rating(Rating, MoodFilteredFilms, FinalFilteredFilms),
    
    % Display recommendations
    display_recommendations(FinalFilteredFilms).
    
filter_by_mood(Mood, Films, FilteredFilms) :-
    mood_genres(Mood, Genres),
    include(has_genre(Genres), Films, FilteredFilms).  

% Predicate to filter films based on user's rating preference
filter_by_rating(Rating, Films, FilteredFilms) :-
    include(has_minimum_rating(Rating), Films, FilteredFilms).

% Predicate for minimum rating criteria
has_minimum_rating(Rating, Film) :-
    rating(Film, FilmRating),
    FilmRating >= Rating.

has_director(Director, Film) :-
    director(Film, Director).

has_actor(Actor, Film) :-
    actor(Film, Actor).

has_year(Year, Film) :-
    year(Film, Year).


before_2000(Film) :-
    year(Film, Year),
    Year < 2000.

after_2000(Film) :-
    year(Film, Year),
    Year >= 2000.

% Predicate to filter films based on user's age
filter_by_age(Age, Films, FilteredFilms) :-
    (   Age < 50 ->
        include(after_2000, Films, FilteredFilms)  % Films from 2000 onwards for age < 50
    ;   include(before_2000, Films, FilteredFilms)  % Films before 2000 for age >= 50
    ).


has_genre(Genres, Film) :-
    genre(Film, Genre),
    member(Genre, Genres).

% Predicate to filter films based on user's gender
filter_by_gender(Gender, Films, FilteredFilms) :-
    gender_genres(Gender, Genres),
    include(has_genre(Genres), Films, FilteredFilms).


query_preferences(UnFilteredList) :-
    writeln('Choose a field to filter by:'),
    writeln('1. Genre'),
    writeln('2. Director'),
    writeln('3. Actor'),
    writeln('4. Year'),
    writeln('5. Rating'),
    writeln('6. Done'),
    read(Choice),
    process_choice(Choice, UnFilteredList).
    
% Process user's choice and update the criteria
process_choice(1, UnFilteredList) :-
    writeln('Enter the genre:'),
    read(Genre),
    include(has_genre([Genre]), UnFilteredList, FilteredList),
    query_preferences(FilteredList).

process_choice(2, UnFilteredList) :-
    writeln('Enter the director:'),
    read(Director),
    include(has_director(Director), UnFilteredList, FilteredList),
    query_preferences(FilteredList).

process_choice(3, UnFilteredList) :-
    writeln('Enter the actor:'),
    read(Actor),
    include(has_actor(Actor), UnFilteredList, FilteredList),
    query_preferences(FilteredList).

process_choice(4, UnFilteredList) :-
    writeln('Enter the year:'),
    read(Year),
    include(has_year(Year), UnFilteredList, FilteredList),
    query_preferences(FilteredList).

process_choice(5, UnFilteredList) :-
    writeln('Enter the rating:'),
    read(Rating),
    include(has_minimum_rating(Rating), UnFilteredList, FilteredList),
    query_preferences(FilteredList).

process_choice(6, UnFilteredList) :-
    display_recommendations(UnFilteredList).
process_choice(_, UnFilteredList) :-
    writeln('Invalid choice. Please try again.'),
    query_preferences(UnFilteredList).
	

