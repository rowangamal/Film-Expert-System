display_recommendations([]) :-
    writeln('No films found matching the criteria.').
display_recommendations(Films) :-
    getNext(Films).

% Predicate to handle user input for the film recommendation
handleList(_, []) :-
    writeln('No more recommendations available.').
handleList('', Films) :-
    write('If you have watched that film before or need another recommendation, write next.'),
    nl, write('Else, write done.'), nl,
    read(IN),
    handleList(IN, Films).

handleList('done', _) :-
    write('Finished'), nl.
handleList('next', Films) :-
    getNext(Films).

% Predicate to get the next film from the list
getNext([]) :-
    writeln('No more recommendations available.').
getNext([H|T]) :-
    printFilm(H),
    handleList('', T).

printFilm(Film):-
    write('current recommendation is: '), nl,
    write('\t'), write(Film), nl,
    genre(Film, Genre), 
    year(Film, Year), 
    rating(Film, Rating),
    findall(Actor, actor(Film, Actor), Actors),
    findall(Director, director(Film, Director), Directors),
    write('\t'), write(Genre), write(' '), write(Year), write(' '), write(Rating), nl,
    write('\tactors:\t'), printList(Actors),
    write('\tdirectors:\t'), printList(Directors), nl.

printList([]).
printList([H|L]):-
    write(H),
    (L = [] -> nl ; write(', ')),
    printList(L).% Start the recommendation system
start :-
    personalInfo_based_Recommendation.

